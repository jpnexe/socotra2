// Función para cambiar el idioma
function changeLanguage() {
    // Obtener el idioma actual
    const currentLang = document.documentElement.lang;
    // Cambiar al otro idioma
    const newLang = currentLang === 'es' ? 'en' : 'es';
    
    // Actualizar el atributo lang del HTML
    document.documentElement.lang = newLang;
    
    // Guardar la preferencia en localStorage
    localStorage.setItem('language', newLang);
    
    // Actualizar todos los textos
    updateTexts(newLang);
}

// Función para actualizar todos los textos según el idioma
function updateTexts(lang) {
    // Actualizar elementos con atributo data-i18n
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Si es un botón con icono, preservar el icono
            if (element.classList.contains('btn-text') && element.innerHTML.includes('<i class')) {
                const iconHtml = element.innerHTML.substring(element.innerHTML.indexOf('<i class'));
                element.innerHTML = translations[lang][key] + ' ' + iconHtml;
            } else {
                element.innerHTML = translations[lang][key];
            }
        }
    });
    
    // Actualizar placeholders de los campos de formulario
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Actualizar opciones de los selects
    document.querySelectorAll('select option[data-i18n]').forEach(option => {
        const key = option.getAttribute('data-i18n');
        if (translations[lang][key]) {
            option.textContent = translations[lang][key];
        }
    });
}

// Función para inicializar el idioma al cargar la página
function initLanguage() {
    // Verificar si hay una preferencia guardada
    const savedLang = localStorage.getItem('language');
    
    if (savedLang && (savedLang === 'es' || savedLang === 'en')) {
        // Si hay una preferencia guardada, usarla
        document.documentElement.lang = savedLang;
        updateTexts(savedLang);
    } else {
        // Si no hay preferencia guardada, usar inglés por defecto
        document.documentElement.lang = 'en';
        updateTexts('en');
    }
    
    // Agregar evento al botón de cambio de idioma
    const langToggle = document.getElementById('language-toggle');
    if (langToggle) {
        langToggle.addEventListener('click', function(e) {
            e.preventDefault();
            changeLanguage();
        });
    }
}

// Inicializar cuando el DOM esté cargado
document.addEventListener('DOMContentLoaded', initLanguage);